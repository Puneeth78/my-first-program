# Basic Arithmetic Operations in Python

# Taking two numbers
a = 15
b = 4

# Addition
print("Addition:", a + b)

# Subtraction
print("Subtraction:", a - b)

# Multiplication
print("Multiplication:", a * b)

# Division (gives float result)
print("Division:", a / b)

# Floor Division (gives integer result)
print("Floor Division:", a // b)

# Modulus (remainder)
print("Modulus:", a % b)

# Exponent (power)
print("Exponent:", a ** b)
